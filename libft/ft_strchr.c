#include "libft.h"

char		*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return ((char *)&s[i]);
		i++;
	}
	if (c == 0 && s[i] == c)
		return ((char *)&s[i]);
	if (s[i] == '\0' && s[i] != c)
		return (NULL);
	return (NULL);
}
