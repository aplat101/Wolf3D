#include "libft.h"

char		*ft_strdup(const char *s)
{
	int		cmp;
	int		i;
	char	*dup;

	cmp = ft_strlen(s);
	if (!(dup = (char *)malloc(sizeof(char) * (cmp + 1))))
		return (NULL);
	i = 0;
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
