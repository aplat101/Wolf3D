#include "libft.h"

char				*ft_strncpy(char *dest, const char *src, size_t n)
{
	char			*init;
	unsigned int	i;

	init = dest;
	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	dest = init;
	return (dest);
}
