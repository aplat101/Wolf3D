#include "libft.h"

void	ft_putstr_fd(const char *s, int fd)
{
	if (!(s))
		return ;
	if (*s != '\0')
	{
		while (*s)
		{
			ft_putchar_fd((unsigned char)*s, fd);
			s++;
		}
	}
}
